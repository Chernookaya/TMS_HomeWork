//2. Необходимо вывести на экран числа от 5 до 1.При решении используйте операцию декремента (--).


public class Cycle_Task2_NumbersInDescendingOrder {
    public static void main(String[] args) {
        int num = 5;
        while (num >= 1) {
            System.out.println(num);
            num--;
        }
    }
}
